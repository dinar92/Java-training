package ru.job4j;

/**Class for determining max value.
*@author gimazetdinov
*@version 1.0
*@since 24.01.2017
*/
public class Max {

	/**
	*Return max of two params.
	*@param first - first arg
	*@param second - second arg
	*@return int value
	*/
	public int max(int first, int second) {
		return first > second ? first : second;
	}
}
